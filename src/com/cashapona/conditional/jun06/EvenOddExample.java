//if else statement
public class EvenOddExample{
	
public static void main(String[] args) {
	
    //defining a variable  
    int number=13;
    
    //Check if the number is divisible by 2 or not  
    if(number%2==0){  
        System.out.println("even number");  
    }else{  
        System.out.println("odd number");  
    }  
}  
}
